package player;

public class Achievement {

	String name;
	int value;
	
	public Achievement(String name, int value) {
		this.name = name;
		this.value = value;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}
	
	
} //Achievement
